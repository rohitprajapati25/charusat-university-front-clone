$(document).ready(function(){
    $("#au").hide();
    $("#a").mouseenter(function () { 
        $("#au").slideDown(); 
    });
    $("#a").mouseleave(function () { 
        $("#au").hide(); 
    });
    $("#au").mouseenter(function () { 
        $("#au").show(); 
    });
    $("#au").mouseleave(function () { 
        $("#au").slideUp(); 
    });
    
});
